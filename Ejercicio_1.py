class Persona:
    def __init__(self, id, name, lname, race, gender) -> None:
        self.id = id
        self.name = name
        self.lname = lname
        self.race = race
        self.gender = gender 
    def __str__(self) -> str:
        return f"{self.name} {self.lname}"
    def show_atributes(self):
        print(f"{self.id} {self.name} {self.lname} {self.race} {self.gender}")

class Estudiante (Persona):
    def __init__(self, id, name, lname, race, gender, id_class, grades={"P1": None, "P2": None, "P3": None}) -> None:
        super().__init__(id, name, lname, race, gender)
        self.id_class = id_class
        self.grades = grades

    def modificar_nota(self):
        corte = input("ingrese el corte: ")
        while not corte in self.grades.keys():
            print("ingreso invalido")
            corte = input("ingrese el corte: ")
        nota = input(f"ingrese la nota para el corte {corte}: ")
        while not nota.isnumeric() or int(nota)>20:
            print("ingreso invalido")
            nota = input(f"ingrese la nota para el corte {corte}: ")
        self.grades[corte]=int(nota)

    def mostrar_notas(self):
        print(f"Notas de {self.name}")
        print("-"*20)
        for i in self.grades:
            print(f"{i}: {self.grades[i]} ptos")
        print("-"*20)

        
class Profesor (Persona):
    def __init__(self, id, name, lname, race, gender, id_class) -> None:
        super().__init__(id, name, lname, race, gender)
        self.id_class = id_class

class Instituto:
    def __init__(self,edd, profesores = [], estudiantes = []) -> None:
        self.profesores = profesores
        self.estudiantes = estudiantes
        self.cargar_profesores(edd)
        self.cargar_estudiantes(edd)
        self.secciones = self.cargar_secciones()
    def cargar_profesores(self, edd):
        for dic in edd["teacher"]:
            self.profesores.append(Profesor(dic["id"], dic["first_name"], dic["last_name"], dic["race"], dic["gender"], dic["id_class"]))
    def cargar_estudiantes(self, edd):
        for dic in edd["student"]:
            self.estudiantes.append(Estudiante(dic["id"], dic["first_name"], dic["last_name"], dic["race"], dic["gender"], dic["id_class"]))
    def cargar_secciones(self):
        aux = []
        for i in self.profesores:
            if i.id_class in aux:
                continue
            aux.append(i.id_class)
        return aux
    
    def mostrar_profesores(self):
        for i in self.profesores:
            print(i)

    def mostrar_estudiantes(self):
        for i in self.estudiantes:
            print(i)

    def gestionar_secciones(self):
        print("-"*20)
        for i in self.secciones:
            print(f"Seccion: {i}")
        print("-"*20)

    def vizualizar_seccion(self):
        seccion = input("ingrese la seccion: ")
        while not seccion.isnumeric() or int(seccion) not in self.secciones:
            print("ingreso invalido")
            seccion = input("ingrese la seccion: ")
        seccion = int(seccion)
        print(f"Seccion {seccion}")
        print("-"*20)
        for prof in self.profesores:
            if seccion==prof.id_class:
                print(f"El profesor: {prof.name} {prof.lname}")
                break
        print("-"*20)
        for est in self.estudiantes:
            if seccion==est.id_class:
                print(f"{est.id}. {est.name} {est.lname}")
        print("-"*20)

    def gestionar_notas_estudiante(self):
        id = input("selecciona el id del estudiante: ")
        while not id.isnumeric() or int(id) not in [est.id for est in self.estudiantes]:
            print("ingreso invalido")
            id = input("selecciona el id del estudiante: ")
        id = int(id)
        for est in self.estudiantes:
            if est.id==id:
                est.modificar_nota()
                est.mostrar_notas()
                break
    def seleccionar_estudiante(self):
        id = input("selecciona el id del estudiante: ")
        while not id.isnumeric() or int(id) not in [est.id for est in self.estudiantes]:
            print("ingreso invalido")
            id = input("selecciona el id del estudiante: ")
        id = int(id)
        for est in self.estudiantes:
            if est.id==id:
                est.mostrar_notas()
                break



